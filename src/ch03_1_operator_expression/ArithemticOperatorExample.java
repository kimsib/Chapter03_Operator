package ch03_1_operator_expression;

public class ArithemticOperatorExample {
	public static void main(String[] args) {
		int v1 = 5;
		int v2 = 2;
		
		int result1 = v1 + v2;
		System.out.printf("result1=" + result1);
		
		int result2 = v1 - v2;
		System.out.printf("result2=" + result1);
		
		int result3 = v1 * v2;
		System.out.printf("result3=" + result1);
		
		int result4 = v1 / v2;
		System.out.printf("result4=" + result1);
		
		int result5 = v1 % v2;
		System.out.printf("result5=" + result1);
		
		double result6 = (double) v1 / v2;
		System.out.printf("result6=" + result1);
		
	}
}
